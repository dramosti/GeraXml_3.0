using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace HLP.GeraXml.bel.NFe.Estrutura
{

    public class belIcms60
    {
        /// <summary>
        /// Tributação do ICMS; 00 - Atribuida Integralmente.
        /// </summary>
        private string _cst;

        /// <summary>
        /// Origem da Mercadoria; 0 - Nacional; 1 - Estrangeira, Importação direta; 2 - Estrangeira, Adquirida no merdado interno.
        /// </summary>
        private string _orig;

        /// <summary>
        /// Valor da BC do ICMS ST.
        /// </summary>
        private decimal _vbcstret;

        /// <summary>
        /// Valor do ICMS ST
        /// </summary>
        private decimal _vicmsstret;

        public string Cst
        {
            get { return _cst; }
            set { _cst = value; }
        }

        public string Orig
        {
            get { return _orig; }
            set { _orig = value; }
        }

        public decimal Vbcstret
        {
            get { return _vbcstret; }
            set { _vbcstret = value; }
        }

        public decimal Vicmsstret
        {
            get { return _vicmsstret; }
            set { _vicmsstret = value; }
        }
    }
}
