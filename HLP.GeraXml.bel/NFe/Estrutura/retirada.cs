using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace HLP.GeraXml.bel.NFe.Estrutura
{
    public class belRetirada
    {
        /// <summary>
        /// CNPJ, Informar os zeros não significativos
        /// </summary>
        private string _cnpj;

        public string Cnpj
        {
            get { return _cnpj; }
            set { _cnpj = value; }
        }
        /// <summary>
        /// Logradouro
        /// </summary>
        private string _xlgr;

        public string Xlgr
        {
            get { return _xlgr; }
            set { _xlgr = value; }
        }
        /// <summary>
        /// Número
        /// </summary>
        private string _nro;

        public string Nro
        {
            get { return _nro; }
            set { _nro = value; }
        }
        /// <summary>
        /// Complementos
        /// </summary>
        private string _xcpl;

        public string Xcpl
        {
            get { return _xcpl; }
            set { _xcpl = value; }
        }
        /// <summary>
        /// Bairro
        /// </summary>
        private string _xbairro;

        public string Xbairro
        {
            get { return _xbairro; }
            set { _xbairro = value; }
        }
        /// <summary>
        /// Município, Utilizar a Tabela do IBGE (Anexo IV - Tabela de UF, Município e País). Informar '9999999' para operaçoes com o exterior.
        /// </summary>
        private string _cmun;

        public string Cmun
        {
            get { return _cmun; }
            set { _cmun = value; }
        }
        /// <summary>
        /// Nome do Município, Informar 'EXTERIOR' , para operações com o exterior
        /// </summary>
        private string _xmun;

        public string Xmun
        {
            get { return _xmun; }
            set { _xmun = value; }
        }
        /// <summary>
        /// Sigla da UF, Informar 'EX' para operaçoes do exterior
        /// </summary>
        private string _uf;

        public string Uf
        {
            get { return _uf; }
            set { _uf = value; }
        }
    }
}
